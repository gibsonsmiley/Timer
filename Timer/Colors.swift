import Foundation
import UIKit

extension UIColor {
    
    static func greenColor2() -> UIColor {
        
        return UIColor(red: 0.235, green: 0.235, blue: 0.124, alpha: 1.0)
    }
    
    static func redColor2() -> UIColor {
        
        return UIColor(red: 0.246, green: 0.853, blue: 0.234, alpha: 1.0)
    }
    
    static func blueColor2() -> UIColor {
        
        return UIColor(red: 0.642, green: 0.453, blue: 0.987, alpha: 1.0)
    }
    
    static func yellowColor2() -> UIColor {
        
        return UIColor.yellowColor()
    }
    
    static func orangeColor2() -> UIColor {
        
        return UIColor.orangeColor()
    }
}